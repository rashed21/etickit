package ticket.com.pojo;

/**
 *
 * @author Emrul
 */
public class VillageInfo {
    private int vid;
    private int ward;
    private String vname;

    public VillageInfo() {
    }

    public int getVid() {
        return vid;
    }

    public void setVid(int vid) {
        this.vid = vid;
    }

    public int getWard() {
        return ward;
    }

    public void setWard(int ward) {
        this.ward = ward;
    }

    public String getVname() {
        return vname;
    }

    public void setVname(String vname) {
        this.vname = vname;
    }
    
    
}
